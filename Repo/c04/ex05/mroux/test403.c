#include <stdio.h>

int		ft_atoi(char *str);

int		main(void)
{
	printf("%d : 12345\n", ft_atoi("12345"));
	printf("%d : -12345\n", ft_atoi("-12345"));
	printf("%d : 0\n", ft_atoi("0"));
	printf("%d : 0\n", ft_atoi("--+ 	+2147483647"));
	printf("%d : -214\n", ft_atoi("-+214 7483647"));
	printf("%d : -2147483648\n", ft_atoi("+---2147483648k02"));
	return 0;
}
