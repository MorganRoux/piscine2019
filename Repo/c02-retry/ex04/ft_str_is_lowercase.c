int		ft_str_is_lowercase(char *str)
{
	int n;

	n = 1;
	while (*str != '\0')
	{
		if (*str < 'a' || *str > 'z')
		{
			n = 0;
			break ;
		}
		str++;
	}
	return (n);
}
